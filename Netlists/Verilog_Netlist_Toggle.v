(* NotValidForBitStream *)
module Switch_Control
   (clock,
    switch);
  input clock;
  output switch;

  wire clock;
  wire clock_IBUF;
  wire clock_IBUF_BUFG;
  wire p_0_in;
  wire switch;
  wire switch_OBUF;

  BUFG clock_IBUF_BUFG_inst
       (.I(clock_IBUF),
        .O(clock_IBUF_BUFG));
  IBUF #(
    .CCIO_EN("TRUE")) 
    clock_IBUF_inst
       (.I(clock),
        .O(clock_IBUF));
  OBUF switch_OBUF_inst
       (.I(switch_OBUF),
        .O(switch));
  LUT1 #(
    .INIT(2'h1)) 
    toggle_i_1
       (.I0(switch_OBUF),
        .O(p_0_in));
  FDRE #(
    .INIT(1'b0)) 
    toggle_reg
       (.C(clock_IBUF_BUFG),
        .CE(1'b1),
        .D(p_0_in),
        .Q(switch_OBUF),
        .R(1'b0));
endmodule
